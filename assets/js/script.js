const getTranslation = (lang, key) => {
  if (translations[lang] && translations[lang][key]) {
    return translations[lang][key];
  }
  if (translations.en && translations.en[key]) {
    return translations.en[key];
  }
  return key;
};

const applyTranslations = (lang) => {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (key) {
      element.textContent = getTranslation(lang, key);
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    const key = element.dataset.i18n;
    const attr = element.dataset.i18nAttr;
    if (key && attr) {
      element.setAttribute(attr, getTranslation(lang, key));
    }
  });
};

const updateGiscusLanguage = (lang) => {
  const iframe = document.querySelector("iframe.giscus-frame");
  if (!iframe) {
    return;
  }
  iframe.contentWindow?.postMessage(
    { giscus: { setConfig: { lang } } },
    "https://giscus.app"
  );
};

const setLanguage = (lang) => {
  document.documentElement.lang = lang;
  localStorage.setItem("preferredLanguage", lang);
  applyTranslations(lang);
  updateGiscusLanguage(lang);
};

const initLanguage = () => {
  const stored = localStorage.getItem("preferredLanguage");
  const startingLang = stored === "en" || stored === "lt" ? stored : "lt";
  setLanguage(startingLang);

  const toggle = document.querySelector(".language-toggle");
  if (toggle) {
    toggle.addEventListener("click", () => {
      const nextLang =
        document.documentElement.lang === "lt" ? "en" : "lt";
      setLanguage(nextLang);
    });
  }
};

const initForms = () => {
  const forms = document.querySelectorAll("form[data-form]");
  forms.forEach((form) => {
    const action = form.getAttribute("action") || "";
    const warning = form.querySelector("[data-form-warning]");
    if (warning && action.includes("yourFormId")) {
      warning.hidden = false;
    }

    if (!window.fetch) {
      return;
    }

    form.addEventListener("submit", async (event) => {
      event.preventDefault();
      const status = form.querySelector("[data-status]");
      const lang = document.documentElement.lang || "lt";
      if (status) {
        status.textContent = getTranslation(lang, "form_sending");
      }

      try {
        const response = await fetch(action, {
          method: form.getAttribute("method") || "POST",
          headers: {
            Accept: "application/json"
          },
          body: new FormData(form)
        });

        if (response.ok) {
          if (status) {
            status.textContent = getTranslation(lang, "form_success");
          }
          form.reset();
        } else {
          if (status) {
            status.textContent = getTranslation(lang, "form_error");
          }
        }
      } catch (error) {
        if (status) {
          status.textContent = getTranslation(lang, "form_error");
        }
      }
    });
  });
};

document.addEventListener("DOMContentLoaded", () => {
  initLanguage();
  initForms();
});
